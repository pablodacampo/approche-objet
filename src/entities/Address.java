package entities;

	public class Address {
		
		public int streetNumber;
		public String streetName;
		public int postCode;
		public String city;
		
		public Address( int newStreetNumber, String newStreetName, int newPostCode, String newCity ) {
			
			this.streetNumber = newStreetNumber;
			this.streetName = newStreetName;
			this.postCode = newPostCode;
			this.city = newCity;
			
			
		}
	

		
		
	}


